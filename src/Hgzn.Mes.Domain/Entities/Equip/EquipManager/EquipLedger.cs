/// <summary>
/// 随机时间生成器
/// </summary>
public class RandomTimeGenerator
{
    private static readonly ThreadLocal<Random> random = new ThreadLocal<Random>(() => new Random());

    /// <summary>
    /// 生成两个时间
    /// </summary>
    /// <param name="minDate"></param>
    /// <param name="maxDate"></param>
    /// <returns></returns>
    public static (DateTime firstTime, DateTime secondTime) GenerateOrderedRandomTimes(
        DateTime minDate, DateTime maxDate)
    {
        var rand = random.Value;

        // 生成第一个随机时间
        double firstRatio = rand.NextDouble();
        DateTime firstTime = minDate.AddTicks((long)((maxDate - minDate).Ticks * firstRatio));

        // 生成第二个时间，确保它大于第一个时间
        double secondRatio = rand.NextDouble();
        DateTime secondTime = firstTime.AddTicks((long)((maxDate - firstTime).Ticks * secondRatio));

        return (firstTime, secondTime);
    }
}
